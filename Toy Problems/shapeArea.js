function shapeArea(n) {
return (1 + (n-1)*n) *2 - 1
}

// n = 5

// 5 - 1 = 4
//  4 * 5 = 20
// 20 + 1 = 21
// 21 * 2 = 42
// 42 - 1 = 41

// n = 100
// 100 - 1 = 99
// 99 * 109 = 9900
// 9900 + 1 = 9901
// 9901 * 2 = 19802
// 19802 - 1 = 19801


